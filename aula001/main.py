#Trabalhando com variáveis em Python
#Operando numeros matematicamente
#Insirindo valores e definindo tipos de variáveis
x = int(input("Insira um valor inteiro de 0 a 9: "))
print("O valor inserido foi:", x)
k = int(input("Insira um valor inteiro negativo de 1 a 9: "))
print("O valor inserido foi:", k)
z = x + k
print("A soma dos valores inseridos é:", z)
w = x * k
print("A multiplicação dos valores inseridos é:", w)
y = x - k
print("A subtração dos valores inseridos é:", y)
d = x / k
print("A divisão dos valores inseridos é:", d)

